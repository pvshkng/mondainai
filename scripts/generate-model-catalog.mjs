// Generates src/main/ai/providers/catalog.ts from the AI SDK provider packages.
//
// For each provider we read the model-ID union that its `createXxx(...)` provider
// helper accepts on `.languageModel(modelId)`, then keep only chat/text models
// (dropping embedding, image, audio/tts, transcription, video, rerank variants).
//
// Run with: node scripts/generate-model-catalog.mjs

import { readFileSync, writeFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const root = resolve(__dirname, '..')

// providerId -> npm package under node_modules. azure is deployment-only (the
// SDK types its models as a bare `string`), so it stays custom-models-only.
const PACKAGES = {
  openai: '@ai-sdk/openai',
  anthropic: '@ai-sdk/anthropic',
  google: '@ai-sdk/google',
  mistral: '@ai-sdk/mistral',
  bedrock: '@ai-sdk/amazon-bedrock',
  vertex: '@ai-sdk/google-vertex'
}

// Non-chat model families to filter out of each language-model union.
const NON_CHAT = /(embed|image|imagen|nano-banana|canvas|tts|audio|whisper|transcribe|chirp|telephony|dall-e|\bveo|lyria|rerank)/i

function readDts(pkg) {
  return readFileSync(resolve(root, 'node_modules', pkg, 'dist/index.d.ts'), 'utf8')
}

// Find `languageModel(modelId: SomeUnionType)` and return `SomeUnionType`.
function languageModelTypeName(dts) {
  const match = dts.match(/languageModel\s*:?\s*\(?\s*\w+\s*:\s*([A-Za-z0-9_]+)/)
  return match ? match[1] : null
}

// Extract the string-literal members of `type Name = 'a' | 'b' | (string & {});`
function unionLiterals(dts, typeName) {
  const decl = dts.match(new RegExp(`type ${typeName}\\s*=\\s*([^;]+);`))
  if (!decl) return []
  const seen = new Set()
  const ids = []
  for (const m of decl[1].matchAll(/'([^']+)'/g)) {
    const id = m[1]
    if (seen.has(id)) continue
    seen.add(id)
    ids.push(id)
  }
  return ids
}

function prettyLabel(providerId) {
  return providerId.charAt(0).toUpperCase() + providerId.slice(1)
}

const entries = {}
for (const [providerId, pkg] of Object.entries(PACKAGES)) {
  const dts = readDts(pkg)
  const typeName = languageModelTypeName(dts)
  if (!typeName) {
    console.warn(`[${providerId}] could not locate languageModel() type; leaving empty`)
    entries[providerId] = []
    continue
  }
  const models = unionLiterals(dts, typeName).filter((id) => !NON_CHAT.test(id))
  entries[providerId] = models
  console.log(`[${providerId}] ${typeName} -> ${models.length} chat models`)
}
// azure has no static catalog (deployment IDs are user-provided).
entries.azure = []

const lines = []
lines.push('// AUTO-GENERATED by scripts/generate-model-catalog.mjs — do not edit by hand.')
lines.push('// Model lists are derived from each @ai-sdk provider\'s languageModel() type,')
lines.push('// filtered to chat/text models. Regenerate with: node scripts/generate-model-catalog.mjs')
lines.push('')
lines.push("import type { ModelInfo } from './types'")
lines.push('')
lines.push('export const CATALOG: Record<string, ModelInfo[]> = {')
const order = ['openai', 'anthropic', 'google', 'mistral', 'bedrock', 'vertex', 'azure']
for (const providerId of order) {
  const models = entries[providerId] ?? []
  if (models.length === 0) {
    lines.push(`  ${providerId}: [],`)
    continue
  }
  lines.push(`  ${providerId}: [`)
  for (const id of models) {
    const label = providerId === 'vertex' ? `${id} (${prettyLabel(providerId)})` : id
    lines.push(`    { id: ${JSON.stringify(id)}, label: ${JSON.stringify(label)} },`)
  }
  lines.push('  ],')
}
lines.push('}')
lines.push('')

writeFileSync(resolve(root, 'src/main/ai/providers/catalog.ts'), lines.join('\n'))
console.log('Wrote src/main/ai/providers/catalog.ts')
